package com.chargeback.vo;

public class OrgVO {
	private String orgName;

	private double orgCpu;

	private double orgMem;

	private double orgDisk;

	public String getOrgName() {
		return orgName;
	}

	public void setOrgName(String orgName) {
		this.orgName = orgName;
	}

	public double getOrgCpu() {
		return orgCpu;
	}

	public void setOrgCpu(double orgCpu) {
		this.orgCpu = orgCpu;
	}

	public double getOrgMem() {
		return orgMem;
	}

	public void setOrgMem(double orgMem) {
		this.orgMem = orgMem;
	}

	public double getOrgDisk() {
		return orgDisk;
	}

	public void setOrgDisk(double orgDisk) {
		this.orgDisk = orgDisk;
	}

}
